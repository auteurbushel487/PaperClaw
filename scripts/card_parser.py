#!/usr/bin/env python3
"""Paper Deep Parser — Knowledge Card Markdown Parser.

Extracts structured JSON fields from card.md files generated by
read-arxiv-paper. This is the deterministic tool layer for the
paper-deep-parser Skill.

The Agent (via SKILL.md) orchestrates:
  1. For each paper, create an independent Session via sessions_spawn
  2. Trigger read-arxiv-paper to generate card.md
  3. Call this script to extract structured JSON from card.md

Extracted Fields:
  - sub_field: Research sub-field classification
  - ID_paradigm: Item ID/representation paradigm
  - item_tokenizer: Tokenization method for items
  - baselines_compared: List of baseline methods compared
  - transferable_techniques: Techniques transferable to other work
  - inspiration_ideas: Ideas inspired by the paper

Usage:
    # Parse a single card.md file
    python card_parser.py --card-path /path/to/card.md --arxiv-id 2305.05065

    # Parse all cards for a pipeline run (batch mode)
    python card_parser.py --run-id {run_id}

    # Parse with explicit output path
    python card_parser.py --card-path /path/to/card.md --output /path/to/output.json
"""

import argparse
import json
import logging
import os
import re
import sys
from pathlib import Path
from typing import Any, Dict, List, Optional

# Standard path injection for paper-agent common modules
_PAPER_AGENT_ROOT = Path(os.environ.get("PAPER_AGENT_ROOT", str(Path(__file__).resolve().parent.parent)))
if str(_PAPER_AGENT_ROOT / "scripts") not in sys.path:
    sys.path.insert(0, str(_PAPER_AGENT_ROOT / "scripts"))

